    private void jbtnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnaddActionPerformed
 
        btnOK.setVisible(true);
        String a = jtxtgrade.getText();
    String hr = jtxthours.getText();
    double hrs =Double.parseDouble(jtxthours.getText());
    
  if(a.equals("A+") || a.equals("B")|| a.equals("B+")|| a.equals("A")
          || a.equals("C+") || a.equals("C")|| a.equals("D+")|| a.equals("D")
          || a.equals("F") && (hrs >0 || hrs <20)){
    try{
        switch (a) {
            case "A+":
                addgrade.addElement(String.valueOf(5.0*hrs));
                addhrs.addElement(hr);
                jList1.setModel(addgrade);
                jList2.setModel(addhrs);
                gradecount=gradecount+5.0*hrs;
                counthrs=counthrs + hrs;
                break;
            case "A":
                addhrs.addElement(hr);
                addgrade.addElement(String.valueOf(4.5*hrs));
                jList1.setModel(addgrade);
                jList2.setModel(addhrs);
                gradecount=gradecount+4.5*hrs;
                counthrs=counthrs+hrs;
                break;
            case "B+":
                addhrs.addElement(hr);
                addgrade.addElement(String.valueOf(4.0*hrs));
                jList1.setModel(addgrade);
                jList2.setModel(addhrs);
                gradecount=gradecount+4.0*hrs;
                counthrs=counthrs+hrs;
                break;
            case "B":
                addhrs.addElement(hr);
                addgrade.addElement(String.valueOf(3.5*hrs));
                jList1.setModel(addgrade);
                jList2.setModel(addhrs);
                gradecount=gradecount+3.5*hrs;
                counthrs=counthrs+hrs;
                break;
            case "C+":
                addhrs.addElement(hr);
                addgrade.addElement(String.valueOf(3.0*hrs));
                jList1.setModel(addgrade);
                jList2.setModel(addhrs);
                gradecount=gradecount+3.0*hrs;
                counthrs=counthrs+hrs;
                break;
            case "C":
                addhrs.addElement(hr);
                addgrade.addElement(String.valueOf(2.5*hrs));
                jList1.setModel(addgrade);
                jList2.setModel(addhrs);
                gradecount=gradecount+2.5*hrs;
                counthrs=counthrs+hrs;
                break;
            case "D+":
                addhrs.addElement(hr);
                addgrade.addElement(String.valueOf(2.0*hrs));
                jList1.setModel(addgrade);
                jList2.setModel(addhrs);
                gradecount=gradecount+2.0*hrs;
                counthrs=counthrs+hrs;
                break;
            case "D":
                addhrs.addElement(hr);
                addgrade.addElement(String.valueOf(1.5*hrs));
                jList1.setModel(addgrade);
                jList2.setModel(addhrs);
                gradecount=gradecount+1.5*hrs;
                counthrs=counthrs+hrs;
                break;
            case "F":
                addhrs.addElement(hr);
                addgrade.addElement(String.valueOf(0.0*hrs));
                jList1.setModel(addgrade);
                jList2.setModel(addhrs);
                gradecount=gradecount+0.0*hrs;
                counthrs=counthrs+hrs;
                break;
            default:
                break;
        }
      }catch(Exception e){
        JOptionPane.showMessageDialog(null, "INVALID INPUT FOR GRADE OR CREDIT HOURS","error", JOptionPane.ERROR_MESSAGE);
      }
     jtxtgrade.setText(null);
      jtxthours.setText(null);
         } else{ 
      
     JOptionPane.showMessageDialog(null, "INVALID INPUT FOR GRADE OR CREDIT HOURS","error", JOptionPane.ERROR_MESSAGE);
            
  }
      
    }//GEN-LAST:event_jbtnaddActionPerformed

