    private void btncalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalculateActionPerformed
       double ans = gradecount/counthrs;
       label.setText(""+fmt.format(ans));
    if(ans>=4.0 && ans<=5.0){
        lblgpa.setText("First Class");
       }
         
         else if(ans>=3.0 && ans<=3.99){
        lblgpa.setText("Second Class Upper");
       }
         else if(ans>=2.0 && ans<=2.99){
        lblgpa.setText("Second Class Lower");
       }
        
          else if(ans>=1.0 && ans<=1.99){
        lblgpa.setText("Pass");
       }
          
          else if(ans>=0.0 && ans<=0.99){
        lblgpa.setText("Attempted HND");
       }
          
        jtxtgrade.setText(null);
      jtxthours.setText(null);
      
      
    }//GEN-LAST:event_btncalculateActionPerformed

